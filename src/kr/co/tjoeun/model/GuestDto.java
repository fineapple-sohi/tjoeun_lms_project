package kr.co.tjoeun.model;

public class GuestDto {
	private String guestId;
	private String guestName;
	private String guestTel;
	private String guestEmail;
	private String guestAddr;
	private String guestPw;
	
	public GuestDto() {}

	public String getGuestId() {
		return guestId;
	}

	public void setGuestId(String guestId) {
		this.guestId = guestId;
	}

	public String getGuestName() {
		return guestName;
	}

	public void setGuestName(String guestName) {
		this.guestName = guestName;
	}

	public String getGuestTel() {
		return guestTel;
	}

	public void setGuestTel(String guestTel) {
		this.guestTel = guestTel;
	}

	public String getGuestEmail() {
		return guestEmail;
	}

	public void setGuestEmail(String guestEmail) {
		this.guestEmail = guestEmail;
	}

	public String getGuestAddr() {
		return guestAddr;
	}

	public void setGuestAddr(String guestAddr) {
		this.guestAddr = guestAddr;
	}

	public String getGuestPw() {
		return guestPw;
	}

	public void setGuestPw(String guestPw) {
		this.guestPw = guestPw;
	}
	
}
